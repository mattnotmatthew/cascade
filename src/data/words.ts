// Helper function to get words starting with a specific letter
export function getWordsStartingWith(letter: string, length: number): string[] {
  const letterLower = letter.toLowerCase();
  let wordList: string[];

  switch (length) {
    case 4:
      wordList = fourLetterWords;
      break;
    case 5:
      wordList = fiveLetterWords;
      break;
    case 6:
      wordList = sixLetterWords;
      break;
    default:
      return [];
  }

  return wordList.filter((word) => word.startsWith(letterLower));
}

// Get a random word starting with a specific letter and of a specific length
export function getRandomWord(letter: string, length: number): string | null {
  const words = getWordsStartingWith(letter, length);
  if (words.length === 0) return null;
  return words[Math.floor(Math.random() * words.length)];
}

// Get a random key word (6-letter word for the top)
export function getRandomKeyWord(): string {
  return keyWords[Math.floor(Math.random() * keyWords.length)];
}


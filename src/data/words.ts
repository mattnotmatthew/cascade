// Get a random cascade word
export function getRandomCascadeWord(): string {
  return cascadeWords[Math.floor(Math.random() * cascadeWords.length)];
}

// Get words that have a specific letter at a specific position
export function getWordsWithLetterAt(
  letter: string,
  position: number,
  length: number
): string[] {
  const letterLower = letter.toLowerCase();
  let wordList: string[];

  switch (length) {
    case 4:
      wordList = fourLetterWords;
      break;
    case 5:
      wordList = fiveLetterWords;
      break;
    case 6:
      wordList = sixLetterWords;
      break;
    default:
      return [];
  }

  return wordList.filter(
    (word) => word.length > position && word[position] === letterLower
  );
}

// Check if a word is valid (exists in any of our word lists)
export function isValidWord(word: string): boolean {
  if (!word) return false;
  const wordLower = word.toLowerCase();

  // Check all word lists
  return (
    fourLetterWords.includes(wordLower) ||
    fiveLetterWords.includes(wordLower) ||
    sixLetterWords.includes(wordLower) ||
    cascadeWords.includes(wordLower) ||
    keyWords.includes(wordLower)
  );
}

// Get all words of a specific length
export function getWordsByLength(length: number): string[] {
  switch (length) {
    case 4:
      return [...fourLetterWords];
    case 5:
      return [...fiveLetterWords, ...cascadeWords];
    case 6:
      return [...sixLetterWords];
    default:
      return [];
  }
}
